    private void bt_erfassenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_erfassenActionPerformed
        // TODO add your handling code here:

        if (cb_activepassive.getSelectedIndex() == 0) {

            Mitglied mitglied = new aktiv(txf_sportart1.getText(), txf_sportart2.getText(), i, txf_name.getText());
            mitglieder.add(mitglied);
        } else {
            Mitglied mitglied = new passiv(Double.valueOf(txf_spendenhoehe.getText()), i, txf_name.getText());
            mitglieder.add(mitglied);
        }

        i++;
        jList1.setListData(mitglieder.toArray());
        String[] names = new String[mitglieder.toArray().length];

        for (int j = 0; j < mitglieder.toArray().length; j++) {
            names[j] = mitglieder.get(j).getName();

        }
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(names);

        cb_selectname.setModel(model);
    }//GEN-LAST:event_bt_erfassenActionPerformed
    private void sperren() {
        if (cb_activepassive.getSelectedIndex() == 0) {
            txf_spendenhoehe.setEnabled(false);
            txf_sportart1.setEnabled(true);
            txf_sportart2.setEnabled(true);
        } else {
            txf_sportart1.setEnabled(false);
            txf_sportart2.setEnabled(false);
            txf_spendenhoehe.setEnabled(true);
        }
    }
    private void cb_activepassiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_activepassiveActionPerformed
        // TODO add your handling code here:
        sperren();
    }//GEN-LAST:event_cb_activepassiveActionPerformed

    private void bt_changenameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_changenameActionPerformed
        // TODO add your handling code here:
        Mitglied mitglied = mitglieder.get(cb_selectname.getSelectedIndex());
        mitglied.setName(txf_selectedname.getText());
        jList1.setListData(mitglieder.toArray());
        String[] names = new String[mitglieder.toArray().length];

        for (int j = 0; j < mitglieder.toArray().length; j++) {
            names[j] = mitglieder.get(j).getName();

        }
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(names);

        cb_selectname.setModel(model);
        
    }//GEN-LAST:event_bt_changenameActionPerformed

